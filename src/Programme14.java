public class Programme14 {
        public static void main(String[] args) {
            for(int i=0;i<i+1;i++){
                for(int j=1;j>=j+1;j++){
                    System.out.print(" ");
                }
                for(int k=1;k<5;k++){
                    System.out.print("*");
                }
                System.out.println();
            }
        }
    }




